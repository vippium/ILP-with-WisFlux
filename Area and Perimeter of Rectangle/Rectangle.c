//Program to find Perimeter and Area of Rectangle with height of 7 inches and width of 5 inches

#include<stdio.h>

int main() {

int h = 7; //Both values are in inches
int w = 5;

printf("The Area of Rectangle is : %d inches", 2*(h+w));
printf("\nThe Perimeter of Rectangle is : %d inches", h*w);

}
